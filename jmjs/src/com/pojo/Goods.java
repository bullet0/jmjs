package com.pojo;

import java.sql.Date;

public class Goods{

	private Integer gId;
	private String gName;
	private Integer gNumber;
	private String gProduce;
	private Date gProductionDate;
	private Date gReleaseDate;
	private String gType;
	private String gUnit;
	private String gRemark;
	private String gSupplier;


	
	
	
	public Integer getgId() {
		return gId;
	}

	public void setgId(Integer gId) {
		this.gId = gId;
	}
	
	
	
	
	
	
	
	public String getgName() {
		return gName;
	}

	public void setgName(String gName) {
		this.gName = gName;
	}
	
	
	
	
	
	
	
	public Integer getgNumber() {
		return gNumber;
	}

	public void setgNumber(Integer gNumber) {
		this.gNumber = gNumber;
	}
	
	
	
	
	
	
	
	public String getgProduce() {
		return gProduce;
	}

	public void setgProduce(String gProduce) {
		this.gProduce = gProduce;
	}
	
	
	
	
	
	
	
	public Date getgProductionDate() {
		return gProductionDate;
	}

	public void setgProductionDate(Date gProductionDate) {
		this.gProductionDate = gProductionDate;
	}
	
	
	
	
	
	
	
	public Date getgReleaseDate() {
		return gReleaseDate;
	}

	public void setgReleaseDate(Date gReleaseDate) {
		this.gReleaseDate = gReleaseDate;
	}
	
	
	
	
	
	
	
	public String getgType() {
		return gType;
	}

	public void setgType(String gType) {
		this.gType = gType;
	}
	
	
	
	
	
	
	
	public String getgUnit() {
		return gUnit;
	}

	public void setgUnit(String gUnit) {
		this.gUnit = gUnit;
	}
	
	
	
	
	
	
	
	public String getgRemark() {
		return gRemark;
	}

	public void setgRemark(String gRemark) {
		this.gRemark = gRemark;
	}
	
	
	
	
	
	
	
	public String getgSupplier() {
		return gSupplier;
	}

	public void setgSupplier(String gSupplier) {
		this.gSupplier = gSupplier;
	}
	
	
	
	

}









